    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
           'country_name' => 'required',
           'country_code' => 'required',
           'country_title' => 'required',
           'country_desc' => 'required'
        ]);
        $country_create = Country::create([
            'country_name' => $request->input('country_name'),
            'country_code' => $request->input('country_code'),
            'country_title' => $request->input('country_title'),
            'country_description' => $request->input('country_desc'),
        ]);
        if ($country_create) {
            return redirect()->back()->with("msg", "Country Create Successfully");
        }
    }

